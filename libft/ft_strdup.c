#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*res;

	i = 0;
	while (s[i] != '\0')
		i++;
	res = (char *)malloc(sizeof(char) * (i + 1));
	if (res)
	{
		i = 0;
		while (s[i] != '\0')
		{
			res[i] = s[i];
			i++;
		}
		res[i] = '\0';
	}
	return (res);
}
