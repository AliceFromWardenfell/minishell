#include "libft.h"

void	*ft_memccpy(void *s1, const void *s2, int c, size_t n)
{
	unsigned char	*temp1;
	unsigned char	*temp2;

	temp1 = (unsigned char *)s1;
	temp2 = (unsigned char *)s2;
	while (n)
	{
		*(temp1++) = *(temp2++);
		if (*(temp1 - 1) == (unsigned char)c)
			return (temp1);
		n--;
	}
	return (NULL);
}
